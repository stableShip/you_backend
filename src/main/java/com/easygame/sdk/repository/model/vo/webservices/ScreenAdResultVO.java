package com.easygame.sdk.repository.model.vo.webservices;

public class ScreenAdResultVO {
	
	private Integer id;
	
	private String imageUrl;
	
	private String downloadUrl;
	
	private Integer openAdTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getDownloadUrl() {
		return downloadUrl;
	}

	public void setDownloadUrl(String downloadUrl) {
		this.downloadUrl = downloadUrl;
	}

	public Integer getOpenAdTime() {
		return openAdTime;
	}

	public void setOpenAdTime(Integer openAdTime) {
		this.openAdTime = openAdTime;
	}
	
}
